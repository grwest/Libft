char	*ft_strrchr(const char *s, int c)
{
	int		i;

	i = 0;
	while (s[i])
		i++;
	while (i >= 0)
	{
		if (s[i] == (char)c)
		{
			s = s + i;
			return ((char *)s);
		}
		i--;
	}
	if ((char)c == '\0')
	{
		s = s + i;
		return ((char *)s);
	}
	return (0);
}
